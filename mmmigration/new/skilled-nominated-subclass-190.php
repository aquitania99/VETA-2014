    	<h2>Skilled Nominated (Subclass 190)</h2>
   	  <p><span class="dropcap1">T</span>here are six states in Australia: <em>New South  Wales (NSW), Queensland (Qld), South Australia (SA), Tasmania (Tas.), Victoria  (Vic.) and Western Australia (WA). </em>Each state of Australia has its own State  Migration Plan, in other words a list of occupations that are deemed to be in  demand in that particular state.  This  occupation list tends to change throughout the program year (July to June each  year), and some occupations may be closed to applications, whilst others may  open.  Therefore it is important to check  these lists to see if your occupation is in demand and if so, if you meet the  requirements to apply.  </p>
   	  <p>&nbsp;</p>
      <h4>The process is as  follows: 
      </h4>
      <ul class="list4">
        <li><strong>Is your occupation on one or  more of the State Migration Plan and do you meet the requirements</strong> – are you  entitled to apply?  </li>
        <li><strong>Points Test </strong>– you need to  calculate the level of points you have – the minimum required is 60</li>
        <li><strong>Skills assessment </strong>– you must  make sure your qualifications and work experience are recognised in Australia</li>
        <li><strong>English test </strong>– if you do not  come from a specified country that is exempt from this, OR if you do not need  this to obtain points.  Even native  speakers may be required to do this test if it means bumping up your points.  At the time of writing IELTS is the only  testing centre approved by the Australian government for the purpose of  permanent residence application.  </li>
        <li><strong>Apply for state sponsorship</strong> –  now or you may leave this till after you have lodged the Expression of Interest  as a state may then elect to sponsor you first</li>
        <li><strong>Apply for Expression of  Interest </strong>– this is an online application and is FREE!  Be warned however – the invitation is only  valid for two months and if you do not have the documents required within this  timeframe you could be penalised.  Be  sure you seek appropriate advice before completing this form.  </li>
        <li><strong>Invitation is generated </strong>– if  not, then you may not apply for permanent residence.  </li>
        <li><strong>Application for sub-class 190  is lodged. </strong> This may be finalised within  a few weeks to a few months or more dependent upon several variables.  </li>
      </ul>
      <blockquote> <p>Once you have been granted your visa, you  will then be obliged to take up residence in the sponsoring state for at least  the next two years.  </p></blockquote> 
