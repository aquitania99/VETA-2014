    	<h2>Employer Nomination Scheme (Subclass 186) ENS</h2>
    	<p>&nbsp;</p>
   	  <p><span class="dropcap1">T</span>he Employer Nomination Scheme (ENS) provides a pathway to permanent residence  for the visa holder in three ways: </p>
   	  <p>&nbsp;</p>
   	  <p>&nbsp;</p>
      <ul class="list4">
        <li><span class="highlight gray">Temporary Residence Transition  Stream (TRT)</span> – this pathway allows the 457 visa holder after two years on this  visa to apply for the permanent residence through further sponsorship by the  employer on the ENS (Employer Nomination Scheme).  The sponsoring employer must demonstrate that  during this two year period training has been carried out of its Australian  permanent resident and citizen staff as submitted in the original sponsorship  application.  The visa applicant must be  paid the market rate for the position ie the salary an Australian permanent  resident or citizen would earn in this role and that this is above the  threshold salary specified for this visa sub-class.  </li>
      </ul>
      <p>&nbsp;</p>
      <ul class="list4">
        <li><span class="highlight gray">Direct Entry stream pathway</span> –  this pathway allows the visa applicant to apply for permanent residence through  sponsorship by the employer through the ENS (Employer Nomination Scheme) by  demonstrating that they have a skills assessment and 3 years of relevant work  experience in their nominated occupation.   A skills assessment is effectively a recognition of their qualifications  and/or work experience by a body in Australia appointed to recognise as  equivalent to Australian qualifications and/or work experience.  The 3 years of work experience in the nomination  occupation may be either in Australia or overseas. </li>
      </ul>
      <p>&nbsp;</p>
      <ul class="list4">
        <li><span class="highlight gray">Labour Agreement or Regional  Migration Agreement stream</span> – this is for those visa holders who have been  sponsored through a negotiated agreement with the Department of Immigration  &amp; Citizenship (DIAC) and are then in a position to apply for ENS (Employer  Nomination Scheme).      </li>
      </ul>
      <blockquote> <p>The minimum age for application for this  visa is 50 years old with very some exceptions.   There is also an English language requirement and a market salary  requirement.  </p></blockquote>
