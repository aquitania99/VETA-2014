    	<h2>Training and Research Visa (Subclass 402) TRV </h2>
        
        <p><span class="dropcap1">T</span>he Training &amp; Research Visa provides the visa applicant with an opportunity to work in a paid internship for one year with an Australian employer.  As the name suggests, the purpose of the visa is to upskill and train the applicant towards a more senior level position than that in which they commence their training.  For example, this may be in a childcare educator Group Leader position, leading towards the position of a Childcare Centre Manager, or someone working in Food &amp; Beverage within the hospitality industry to train towards the position of Restaurant or Hotel Manager.   All of the training is on the job, the work is full-time or at least must be a minimum of 30 hours per week, and the salary is paid in accordance with Australian award wages ie employment legislation that applies to Australian permanent resident/citizen staff also.  </p>
        <p>&nbsp;</p>
        <blockquote>This is an ideal visa for both applicant and employer alike to have a keen and enthusiastic graduate training on the job in the workplace. </blockquote>
        <p>No IELTS (English examination) is required to apply for this Training &amp; Research Visa. </p>
      <p>At the end of the traineeship there is the opportunity for the employer to sponsor the applicant to continue working with them on a temporary basis on a 457 employer sponsored visa or RSMS Regional Skilled Migration Scheme for a permanent visa if the traineeship is in a regional area as classified by the Department of Immigration &amp; Citizenship (DIAC).</p>
      <p>&nbsp;</p>
        <h3> <br>
      <strong class="highlight gray">To summarise: </strong>      </h3>
      <ul class="list4">
        <li> Paid internship</li>
         <li>	Must work minimum 30 hours per week</li>
          <li>Sponsor may be different entity to employer eg college</li>
         <li>Training is on the job (does not have to be classroom based)</li>
         <li>Salary paid in accordance with award wage for the position</li>
         <li>No IELTS (English examination) required – employer needs to attest to English language level</li>
         <li>May include family in the visa – partners or spouses have work limitation of 40 hours per fortnight</li>
        </li>
