      <h2>Graduate Visa (Subclass 485)</h2>
      <p><span class="dropcap1">W</span><strong>hat is this? </strong> It is a visa that is designed to allow you to  work full-time on a temporary basis in Australia post-study.</p>
      <p>  </p>
      <p>It&rsquo;s just recently  been updated as a result of the Knight Review, a study carried out by Michael  Knight, former Minister of the Olympics, who was given the task of reviewing  student visas and all things related and came up with a list of 37  recommendations, all of which were adopted by the Australian government, in  their own fashion.</p>
      <p>  </p>
      <p>So firstly it&rsquo;s  important to establish that to be eligible to apply for this visa, you must  hold a student visa and have completed your course.  And not just any old student visa – it must  be a visa that you have held to study a Diploma or higher level course – not  English only for example.  It&rsquo;s important  to be careful about this as some people have been caught out and their  applications declared invalid because they did not hold the right visa.  </p>
      <p>&nbsp;</p>
      <p>In addition, not  only do you have to have studied, you must have fulfilled the Australian study  requirement, which means that you need to have studied a CRICOS registered  course for 92 academic weeks.  CRICOS is the Commonwealth Register of  Institutions and Courses for Overseas Students and is the official Australian  government register of courses that may be studied by all students from  overseas.  Those courses that are not  CRICOS registered do not fulfil the requirements for the grant of a student  visa. </p>
      <p>&nbsp; </p>
      <p>There is an  English level of IELTS 6.0 in all bands required, except for those students  whose passport is on the gazetted list for those exempted from this, such as  the UK and Ireland.  </p>
      <p>&nbsp;</p>
      <p>Before going any  further in terms of these requirements, it is important to differentiate  between the two streams of 485 visas, as released on 24 March 2013.  Prior to this there was only one type of 485  visa.  </p>
      <p>&nbsp;</p>
      <p><strong>So what are these  two streams? </strong></p>
      <p>&nbsp;</p>
      <ol>
        <li>Graduate Work Stream</li>
        <li>Post-study  Work  Stream</li>
      </ol>
      <p>&nbsp;</p>
      <strong class="highlight gray">Graduate Work  Stream</strong>
      <p>&nbsp;</p>
      <p>This is a visa  that has a duration of 18 months.  It is  designed for those student visa holders who have studied in a qualification  that is related to their nominated occupation which has to be on the CSOL and  in which they have the skills required.   A skills assessment (i.e.  recognition of these skills and qualifications  by the relevant assessing authority, depending upon the nominated  occupation) is a pre-requisite to  apply.  In addition, it is important that  the qualifications and skills be closely related to the nominated occupation,  and DIAC seem to have differing opinions on this dependent upon the case  officer.  To be handled with extreme  caution! </p>
      <p>&nbsp; </p>
      <p>&nbsp;</p>
      <strong class="highlight gray">Post-study Work  Stream</strong>
      <p>&nbsp;</p>
      <p>This stream has  been designed for those who have studied for two years or 92 academic weeks (as  long as the course is no less than 16 months and registered as a minimum of 92  weeks on CRICOS this satisfies the requirements – the 92 weeks on CRICOS and  how this is demonstrated by education providers is open for debate so make sure you get good advice on this before  proceeding) at a recognised institution and have the required IELTS English  level of 6.0 in all bands.  There is a  gazetted list of which institutions are recognised for this purpose by the  Australian government and this is regularly updated.  </p>
      <p>&nbsp;</p>
      <p>It is very  important to note that this visa is only for those student visa holders who  held their very first student visa after 5 November 2011.  It is available for up to 4 years dependent upon what you were studying.  For example, if you studied a Bachelor  degree, you will be granted a visa for two years, whereas if you studied a PHd  you will be given a visa for the maximum 4 years to work full-time.  </p>
      <p>&nbsp;</p>
      <blockquote>The great  advantage of this visa is that it does allow you to obtain Australian work  experience, and if you are wishing to stay on and you have the opportunity to  be sponsored by an Australian business, all the better for you and the  business.  </blockquote>
