      <h2>Regional Australia</h2>
      <p>&nbsp;</p>
      <p><span class="dropcap1">I</span> t seems to be on the tip of everyone&rsquo;s lips  these days – from the media, to government, and to international students and  overseas skilled people wishing to migrate to Australia. <strong> So what&rsquo;s so great about going to regional  Australia?</strong></p>
      <p>&nbsp;</p>
      <p><em>Well,  </em>the government has made several policy initiatives to further the development  of regional Australia and encourage the population, not just migrants, to seek  opportunities outside of the metropolitan hubs such as Sydney, Melbourne and  Brisbane.  It&rsquo;s getting pretty crowded in  these cities and the cost of living is skyrocketing. </p>
      <p>&nbsp; </p>
      <p>The  advantages of regional area living for the modern migrant are many: cheaper  housing, lower cost of living, safer areas for bringing up a family, less  crowded, fresh, clean air, simpler transport options and less travel time to  work.  There are of course drawbacks too:  smaller ethnic-based communities therefore perhaps less of one&rsquo;s own  nationality with whom to socialise, network and assist in the settling in  process, though this is changing too with the increase in uptake of regional  visas; less populated therefore less jobs, though for those who are sponsored  in a regional area, this would not affect them.   Even the perception of locals being less receptive to migrants is  changing in regional areas, again as, over time, an increasing number of  migrants tend to be settling in these areas.   Don&rsquo;t forget too, that &ldquo;regional areas&rdquo; include cities that are  themselves growing fast, such as <em>Adelaide, Perth, Canberra and Darwin.  </em></p>
      <p>&nbsp;</p>
      <p><strong>So  what are the visas that can take our modern migrant to such regional  areas? </strong> The main and most popular one,  given the government&rsquo;s push for employer sponsorship rather than independent  skilled migration, is probably the RSMS – Regional Sponsored Migration Scheme,  designed to assist employers in regional areas to sponsor overseas employees in  jobs where they have been unable to place an Australian permanent  resident/citizen.  The visa is available  to any occupation that fits into ANZSCO Skill Level 1-3.  ANZSCO is the guide that is used by DIAC,  amongst other organisations, to assist in defining occupations for the purpose  of temporary or permanent migration. These are positions that are generally  highly skilled such as managerial,engineers, IT specialists, teachers, trades –  interestingly the position of Migration Agent fits into ANZSCO Skill Level 2! To  make an application, the employer must be able to offer a position to the  employee for a minimum of two years, and prove that they have not been able to  find an Australian permanent resident/citizen to fill the position. The  position must be paid the market salary, in many cases the award wage, and a  local Regional Development Australia office must give advice to DIAC that they  have cited the required documentation and the employer has fulfilled the  requirements.  There are two ways for the  visa applicant to apply – either after two years of holding a 457 temporary  employer sponsored visa with the same employer, or direct entry, providing they  are either a native speaker of English from a designated country or  alternatively have obtained a result of 6.0 in all bands in the IELTS exam, the  English examination designated for this purpose by DIAC, and have the skills  required for the position in accordance with the ANZSCO guide.  This visa is subject to cancellation  provisions – in other words, if the visa holder should leave the job and more  specifically the area in which they have been sponsored within the first two  years of the life of the visa, it may be cancelled.  Routine monitoring of these visas takes place  on a regular basis.</p>
      <p>  </p>
      <p>This  visa is proving popular with international students who are seeking their  fortunes in areas outside of the big cities to take advantage of what may be on  offer and have the skills available to fulfil the requirements.  </p>
      <p>&nbsp;</p>
      <p>Other  visas in regional areas are the Skilled-Nominated sub-class 190, which is the  state sponsored skilled migration visa.  There  is a list of occupations in Schedule 2 of the 1 July 2012 CSOL (Consolidated  Skilled Occupations List) which allows for states who have these occupations  listed on their State Migration Plan, to sponsor applicants for the visa.  Applicants must still, however, pass through  the Expression of Interest and obtain an invitation in order to be eligible to  apply.  </p>
      <p>&nbsp;</p>
      <p>Another  option is the Skilled-Nominated (Provisional) sub-class 489 visa which is  points-based and temporary, and designed for skilled workers nominated by a  state or territory or sponsored by an eligible relative living in a designated  area in Australia.  It is valid for 4  years and the visa holder must live in this designated area of Australia.  </p>
      <p>&nbsp;</p>
      <p>Furthermore  there are new initiatives to encourage business people to settle in regional  areas, under the Business Development Sponsorship scheme.  </p>
      <p>&nbsp;</p>
      <blockquote>In  my own practice I find myself advising clients to try out regional areas to  live – Sydney is not the be all and end all!   There is much to Australia outside of the metropolitan hubs and many  advantages to be gained by seeking opportunities in regional areas.</blockquote>
