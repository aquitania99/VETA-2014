    	<h2>Regional Sponsored Migration Scheme (Subclass 187) RSMS</h2>
    	<p>&nbsp;</p>
   	  <p><span class="dropcap1">T</span>he Australian government has clearly been  promoting the uptake of this particular visa sub-class, which fulfils their  objectives of populating the regional areas of Australia and making sure the  big cities do not get too crowded!  By  regional, the Department of Immigration &amp; Citizenship (DIAC) are not just  referring to small towns in the middle of nowhere, but also cities such as  Perth, Adelaide, Canberra, and Darwin.   The metropolitan areas such as Sydney, Brisbane and Melbourne are not  eligible for this category.  Essentially  this is a permanent type of employer sponsorship but in a regional area, or one  that is deemed regional by DIAC.  The  caveat is that the visa holder must remain working with the same employer for  two years since this visa has cancellation provisions in the event that the  visa holder decides, for instance, to move to Sydney or another non-regional  area during the initial two year period after visa grant.     However it provides an excellent  opportunity not only for employers who may find it harder to recruit and to  retain Australian permanent resident or citizen staff in more regional areas,  as well as for visa applicants who are happy to live outside of the main cities  and take advantage of the opportunity to move directly to a permanent  visa.  </p>
   	  <p><br>
      <blockquote> <em>Refer  to article written by MMMigration CEO &amp; Principal Migration Agent Melanie  Macfarlane published in Brazilian magazine Falamos portugues </em><br></blockquote> 
      <p><strong>This is a 3 part process.  </strong></p>
      <p>&nbsp;</p>
      <h3><br>
        <strong class="highlight gray">RCB</strong></h3>
      <p>The Regional Certifying Body in the area  where the job is being carried out needs to provide advice to the Department of  Immigration &amp; Citizenship (DIAC) about the nominating employer and the  nominated position.  The RCB is a body  nominated by DIAC to carry out this role as they are in possession of local  knowledge about the area, employers, market salaries and types of positions  vacant in order to be able to advise about the nominated occupation.  Each has its own checklist of requirements  which can usually be summarised as follows: </p>
      </p>
      <ul class="list4">
        <li>Employer to show adequate  finances to support Australian permanent resident and citizen staff as well as  pay the nominated overseas employee (visa applicant).</li>
        <li>Employer to provide a letter of  offer to the nominated employee for a position for the next two years.</li>
        <li>Employer to provide evidence of  business operation such as business registration certificates, ASIC  registration and Business Activity Statements.</li>
        <li>Employer to provide evidence of  genuine vacancy ie that the position has been previously advertised and has not  been able to be filled by the local market ie Australian citizens or permanent  residents.</li>
      </ul>
      <p>Once the <strong>RCB </strong>approves the above  requirements, the advice is then sent to DIAC to process the second part of the  application process, the nomination. </p>
      <p>&nbsp;</p>
      <p>&nbsp;</p>
      <h3><strong class="highlight gray">Nomination</strong>
      </h3>
      <p>Similar requirements to those of the RCB  are required for the nomination: </p>
      <p>&nbsp;</p>
      <ul class="list4">
        <li>Employer to show adequate  finances to support Australian permanent resident and citizen staff as well as  pay the nominated overseas employee (visa applicant)</li>
        <li>Employer to provide a letter of  offer to the nominated employee for a position for the next two years</li>
        <li>Employer to provide evidence of  business operation such as business registration certificates, ASIC  registration and Business Activity Statements as well as bank statements</li>
        <li>Employer to provide evidence of  genuine vacancy ie that the position has been previously advertised and has not  been able to be filled by the local market ie Australian citizens or permanent  residents</li>
        <li>Advice from the Regional  Certifying Body (RCB) indicating that they have approved the regional  appointment </li>
      </ul>
      <p>DIAC must be satisfied that the position is  genuinely vacant and has not been created simply for the purpose of placing an  overseas employee.  This is usually the  case since regional positions are not as in demand and popular as those in the  larger cities on the Eastern seaboard ie Sydney, Melbourne &amp; Brisbane.  Once DIAC are satisfied that the employer  fulfils all of the above requirements, then they would proceed to the third and  final part of the process, the visa application.  </p>
      <p>&nbsp;</p>
      <h3><br>
        <strong class="highlight gray">Visa  sub-class 187</strong>
      </h3>
      <p>The following are the some of the main requirements  for the grant of a 187 visa:</p>
      <p>&nbsp; </p>
      <ul class="list4">
        <li>Minimum Diploma </li>
        <li>IELTS (English examination) 6.0  in all bands UNLESS the applicant has been sponsored by the employer on a 457  visa for two years, in which case the English requirement is IELTS 5.0 in all  bands</li>
        <li>Letter of offer from employer  for two years</li>
        <li>Satisfy health and character  requirements</li>
      </ul>
      To reiterate, this is a permanent residence  visa, however it has a cancellation condition on the visa which means that if  you do not remain with the nominating employer for two years, the visa may be  cancelled.

