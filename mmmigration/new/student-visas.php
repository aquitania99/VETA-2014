    	<h2>Student Visas</h2>
    	<p>&nbsp;</p>
   	  <p><span class="dropcap1">T</span>he Student visa is for applicants from  overseas who wish to continue their studies in Australia.  These would range from high school students  up to Vocational educational level and further tertiary level such as Bachelor  or Master degrees, up to PHd students.  </p>
   	  <p><br>
      Currently, the way these visas are assessed  is through what is called Assessment Levels, from 1 through to 5.  There are no countries at present and never  have been in Assessment Level 5 which is just as well as the requirements are  almost impossible to meet!  Depending  upon your country of citizenship and passport, you will be allocated an  Assessment Level.  These levels are based  upon the risk associated with certain countries, in accordance with records of  citizens of those countries for example overstaying their visas, not complying  with the various conditions of their visas whilst studying, the likelihood of  applying for a refugee visa when already in Australia, and so on. Related to  the Assessment Levels are different sub-classes of visas in accordance with  what you are planning to study.  </p>
   	  <p>&nbsp;</p>
   	  <p>For  example, if you were to study at university level such as a Bachelor degree or  Masters with Coursework, this would be sub-class 573, whereas to study a PHd  would be sub-class 574.  Then to study a  Diploma at a Vocational Education provider you would be granted a sub-class 572  visa.  All student visas fall under one  class, TU, but under the different sub-classes such as those listed above. Each  sub-class may relate to different sub-classes, even for the same passport  holder.   To give you an example, the UK  is deemed to be Assessment Level 1 and is across the board for all sub-classes  ie anything you would study in Australia if you were from the UK.  Assessment Level 1 is deemed to be the lowest  risk country and the requirements are less onerous than for those from higher  risk countries.  For example, a student  from Colombia may be Assessment level 2 for sub-class 573 to study at  university, but Assessment Level 3 to study a Diploma at a VET sector  provider.  For each Assessment level  there are different requirements in accordance to English level, academic  background and finances that need to be shown for the purpose of obtaining the  visa.  Then there are the more recent  exemptions to these requirements which commenced on 24 March 2012 whereby any  student studying a Master degree in Australia may be exempted from all  requirements despite their Assessment level!   Complicated isn&rsquo;t it!  And of  course this is the theory behind it, but it doesn&rsquo;t always work like this in  practice, because offshore Australian Embassies processing student visas all  have their own standards and methods of assessment, which are not always consistent  with the general policy.  This is  something DIAC is trying to rectify currently – wouldn&rsquo;t it make sense to have  consistent rules applied for the same visa in every country&rsquo;s Australian  Embassy?  </p>
   	  <p><br>
      On 5 November 2011 the Genuine Temporary  Entrant requirement was introduced which was designed to filter out those  non-genuine students as the name suggests, by looking at a student&rsquo;s education  and professional background to see how this fits in with what they wish to  study, their immigration background and their close personal ties to their own  country, in other words their reasons for returning to their own country.  It is really important to understand how this  regulation functions, whether you are applying   for a student visa for the first time from overseas or applying once  more in Australia.  </p>
   	  <p><br>
   	    The Assessment level system is currently  under review and is due to undergo significant changes in the future, which  will no doubt benefit those students passionate about coming to study in this  great country, Australia.  <br>
      With all the recent changes to the student  visa, Australian education institutions have never been under so much pressure  to maintain and improve the quality of the education provided not only to their  international students but also their domestic, and so ultimately this can only  be seen in a positive light.  </p>
