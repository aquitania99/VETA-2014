    	<h2>Skilled Temporary Work visa (Subclass 457)</h2>
    	<p>&nbsp;</p>
    	<p><span class="dropcap1">T</span>he Temporary Work (Skilled) visa (subclass 457) is a visa that was initially  conceived by former Australian Prime Minister Paul Keating and then implemented  by former Prime Minister John Howard&rsquo;s government in 1996. Its original purpose  was as a temporary solution to fill a skills shortage in Australia whilst  Australians were being upskilled and trained in the areas in demand.  </p>
   	  <p>&nbsp;</p>
   	  <p>However, since its inception the visa has  grown from strength to strength hand in hand with the Australian economy and  now sees on average 100,000 being processed per year plus family members.  Its purpose is still to fill positions that  are in demand with overseas employees whose skills fit the position description  and where an Australian permanent resident or citizen is not available to  fulfill the position.  As its name  suggests, it is a temporary visa with a duration of maximum 4 years that also  allows you to bring family with you and has several requirements.</p>
      <h4><br>
Possibly one of the most complex of the  sponsorship visas to apply for, it is a 3 part process: 
      </h4>
      <h3><br>
        <strong class="highlight gray">Sponsorship</strong>
      </h3>
        <p>The company needs to apply for  sponsorship.  The sponsorship approval  usually lasts 3 years if the company has been in existence more than 12 months,  or 12 months if it is a start up business (ie trading for less than 12  months).  The number of employees to be  sponsored must be specified from the outset and approved over the 3 year  period.  <strong>The following are some basic  requirements to fulfil: </strong></p>
      <p>&nbsp;</p>
      <ul class="list4">
        <li>If the business has operated  more than 12 months, a training requirement must be demonstrated ie 1% of  payroll in the last 12 months has been spent on training, whether external or  inhouse.   </li>
        <li>The business must be able to  demonstrate that it has adequate finances in place both to pay its own staff as  well as the potential sponsored staff and that it is in a secure position  financially</li>
        <li>The business must specify how  many positions are to be nominated during the life of the sponsorship and  justify why this is so</li>
      </ul>
      <h3><strong class="highlight gray">Nomination</strong>
      </h3>
      <p>The second part is to nominate the  occupation that the visa applicant will be carrying out.  This occupation is on a gazetted list of  occupations called the CSOL (Consolidated Skilled Occupations List) – <em>here can you please link to this</em> – and  must reflect directly the duties and tasks of the role.  <strong>The following are some basic requirements: </strong></p>
      <p>&nbsp;</p>
        <ul class="list4">
          <li>The business must pay the  market rate of the nominated occupation to the visa applicant.  In other words, what an Australian permanent  resident or citizen would be receiving in this same role.  For example, if there is already one person  within the company carrying out this role.  </li>
          <li>The business must not pay lower  than the TSMIT – this refers to the minimum threshold salary that must be  paid.  This usually increases by 5% each  year.    </li>
          <li>Labour Market Testing – the  business must show evidence that an Australian permanent resident or citizen is  unable to fill the position – this may be through previous advertising on  relevant websites and other means</li>
      </ul>
      <h3><strong class="highlight gray">Visa</strong>
      </h3>
        <p>The third and final part of this process is  the visa application.  Here it is  important to ensure that the visa applicant is eligible for the position, in  other words that they hold the correct qualifications and/or work experience in  order to carry out the role specified.   Depending upon the nominated occupation and the salary level, the  applicant, unless from a specified country where English is the spoken  language, may need to demonstrate a level of English through IELTS, an English  examination specified for the purpose of this visa.  Moreover if the applicant is in or outside  Australia it is important to determine their visa history, or what type of visa  they currently hold, in order to be eligible to apply.</p>
  
<h3><br>
  <span class="highlight gray">To summarise: </span>
</h3>
<p>&nbsp;</p>
        <ul class="list4">
          <li>Visa applicant must have  qualifications and/or work experience to carry out the job</li>
          <li>Visa applicant may be required  to pass English test IELTS dependent upon occupation</li>
      </ul>
        <blockquote><p>The above 3 part process is very much interlinked  and one process must marry up with the other.   It is very important to understand how this process works prior to  lodging this type of 457 visa application.   The 457 may lead to further sponsorship for a permanent residence visa,  through the ENS (Employer Nomination Scheme) sub-class 186.  <br>
        </p></blockquote>
