    	<h2>Partner visas and Family Migration</h2>
    	<p>&nbsp;</p>
   	  <p><span class="dropcap1">T</span>he Australian government has allocated  places annually in its migration program to family of Australian permanent  residents and citizens.  </p>
   	  <p>&nbsp;</p>
   	  <p><strong>This includes  the following: </strong></p>
   	  <p>&nbsp;</p>
   	  <ul class="list4">
   	    <li>Partner visas</li>
   	    <li>Parent and Parent Contributory Visas<br>
   	      </li>
   	    <li>Remaining Relative<br>
   	      </li>
   	    <li>Carer</li>
      </ul>
   	  <h3><br>
      <strong class="highlight gray">Partner  visas</strong></h3>
   	  <p>These are for defacto partners or spouses  of Australian permanent residents or citizens.   </p>
   	  <p>&nbsp;</p>
   	  <p><strong>There are two main types of visa in this category: </strong></p>
      <ul class="list4">
        <li>Prospective Spouse visa</li>
        <li>Defacto/Spouse visa (temporary  and permanent application together).</li>
  
      </ul>
      <h4>Prospective  Spouse visa</h4>
      <p>This visa, commonly known as the fiancé  visa, is for partners of Australian permanent residents/citizens who are  overseas, have become engaged to be married, and are able to demonstrate that  the marriage will take place within 9 months of their arriving in the country,  and other evidence that they are in a genuine relationship.  Once they have arrived onshore in Australia,  they must marry within 9 months, and may then be eligible to apply at a lower  fee for the Spouse Visa (temporary and permanent application at the same time). </p>
      <p>&nbsp;</p>
      <h4><br>
Defacto/spouse  visa
      </h4>
      <p>This visa is for the defacto or spouse  partner of an Australian permanent resident or citizen who has been either  living together for 12 months with their partner, or they have married and  perhaps have less time living together.   There is the opportunity potentially for those who have been living  together for less than 12 months but are not married to apply for a  relationship certificate with the Department of Births, Deaths and Marriage in  their partner&rsquo;s state, however it is important to satisfy the genuine  relationship requirement as well as evidence their future commitment together  as a couple. These applications are very document-heavy and do not be fooled  into believing that just because your relationship is genuine, you do not need  to submit much documentation.  It is  important to emphasise the living together and financial aspect of the  relationship.</p>
      <p>  <br>
        The application is for a temporary and  permanent visa at the same time.  If you  can prove that you have been together in a genuine relationship for at least 3  years, it is possible to be granted a permanent visa immediately, however  otherwise, the visa will be granted on a temporary basis, and within 2 years of  lodgement of the application DIAC (Department of Immigration &amp; Citizenship)  will follow up with a letter to evidence the second part of the application,  for the permanent visa.  In other words,  further evidence of having lived together for these two years is required to  grant the permanent visa.  </p>
      <p>&nbsp;</p>
      <h3><br>
        <strong class="highlight gray">Parent  Visas</strong>
      </h3>
      <p><br>
        Currently the queue for these visas is 15  years!  Some people opt for this because  it is not as expensive as its cousin, the Parent Contributory Visa.  The Parent Visa is for the parent(s) of an  Australian permanent resident (must be after 12 months of grant of permanent  residence and living in Australia) or citizen who wish to reside permanently in  Australia with their child(ren).  Any  applicant must pass the Balance of Family Test, which essentially requires more  than 50% of their children to be residing in Australia as permanent residents  or citizens.  <em>For example, if the  applicant has three children and two are Australian citizens, they would pass  this test. </em></p>
      <p>&nbsp; </p>
      <h3><br>
        <strong class="highlight gray">Parent  Contributory Visas</strong>
      </h3>
      <p><br>
        Many elect to apply for this visa because,  despite the enormous cost (approx. $42,000 per person) the queue is much  shorter – between 1 to 3 years as opposed to 15 years for the Parent Visa.  Once again, in order to satisfy the  requirements for this visa, any applicant must pass the Balance of Family Test,  which essentially requires more than 50% of their children to be residing in  Australia as permanent residents or citizens.   For example, if the applicant has three children and two are Australian  citizens, they would pass this test.</p>
      <p><br>
        The contribution of approx. $42,000 which  goes towards any medical expenses must be paid prior to the visa being  granted.  It is possible to pay this in  two instalments, which involves applying for the temporary parent contributory  visa, followed within two years by the permanent parent contributory visa.  </p>
